#include "libftprintf.h"

int		ft_find_nbr(const char *str)
{
	int	i;
	int	nbr;

	i = 0;
	nbr = 0;
	while ((str[i] >= 48 && str[i] <= 57))
	{
		nbr *= 10;
		nbr += str[i] - '0';
		i++;
	}
	return (nbr);
}
